import LOOKUP_RAW from "./vizcast_lookup_tables_v2.json" with { type: "json" };
const LOOKUP: Record<string, SpotTable> = LOOKUP_RAW.spots;

// ── Types ─────────────────────────────────────────────────────────────────────
interface GridDef {
  swell_h: number[];
  swell_p: number[];
  rain_24h: number[];
  days_since_rain: number[];
  wind_onshore: number[];
  wind_longshore: number[];
  sst: number[];
}

interface RiverConfig {
  gauges: string[];
  flow_median: number;
  flow_p90: number;
  flow_scale: number;
  best_lag: number;
  use_fnu: boolean;
  fnu_gauge: string | null;
}

interface SpotTable {
  label: string;
  coast: number;
  papa: number;
  depth_m: number;
  n_obs: number;
  ntu_median: number;
  sst_median: number;
  grids: GridDef;
  swell_table: number[][];
  rain_table: number[][];
  wind_table: number[][];
  sst_table: number[];
  river: RiverConfig | null;
}

// ── Interpolation helpers ─────────────────────────────────────────────────────
function interp1d(grid: number[], values: number[], x: number): number {
  if (x <= grid[0]) return values[0];
  if (x >= grid[grid.length - 1]) return values[values.length - 1];
  for (let i = 0; i < grid.length - 1; i++) {
    if (x >= grid[i] && x <= grid[i + 1]) {
      const t = (x - grid[i]) / (grid[i + 1] - grid[i]);
      return values[i] * (1 - t) + values[i + 1] * t;
    }
  }
  return values[values.length - 1];
}

function interp2d(
  gridX: number[], gridY: number[], table: number[][], x: number, y: number
): number {
  const cx = Math.max(gridX[0], Math.min(gridX[gridX.length - 1], x));
  const cy = Math.max(gridY[0], Math.min(gridY[gridY.length - 1], y));
  let xi = gridX.length - 2;
  for (let i = 0; i < gridX.length - 1; i++) {
    if (cx >= gridX[i] && cx <= gridX[i + 1]) { xi = i; break; }
  }
  let yi = gridY.length - 2;
  for (let i = 0; i < gridY.length - 1; i++) {
    if (cy >= gridY[i] && cy <= gridY[i + 1]) { yi = i; break; }
  }
  const tx = gridX[xi + 1] !== gridX[xi] ? (cx - gridX[xi]) / (gridX[xi + 1] - gridX[xi]) : 0;
  const ty = gridY[yi + 1] !== gridY[yi] ? (cy - gridY[yi]) / (gridY[yi + 1] - gridY[yi]) : 0;
  return table[xi][yi]     * (1 - tx) * (1 - ty)
       + table[xi + 1][yi] * tx       * (1 - ty)
       + table[xi][yi + 1] * (1 - tx) * ty
       + table[xi + 1][yi + 1] * tx   * ty;
}

// ── Wind decomposition ────────────────────────────────────────────────────────
function decomposeWind(
  wind_spd_kmh: number, wind_dir_deg: number, coast_bearing: number
): { onshore: number; longshore: number } {
  const diffRad = (wind_dir_deg - coast_bearing) * Math.PI / 180;
  return {
    onshore:   Math.max(0, wind_spd_kmh * Math.cos(diffRad)),
    longshore: wind_spd_kmh * Math.sin(diffRad),
  };
}

// ── River flow → effective rain equivalent ────────────────────────────────────
function riverFlowToRainEquiv(
  spotRiver: RiverConfig | null,
  riverFlows: Record<string, number[]> | null | undefined
): number {
  if (!spotRiver || !riverFlows) return 0;
  const lag = spotRiver.best_lag ?? 0;
  let total_flow = 0, n_gauges = 0;
  for (const gaugeId of (spotRiver.gauges ?? [])) {
    const flows = riverFlows[gaugeId];
    if (flows && flows[lag] != null && !isNaN(flows[lag])) {
      total_flow += flows[lag];
      n_gauges++;
    }
  }
  if (n_gauges === 0) return 0;
  const flow_above = Math.max(0, total_flow - (spotRiver.flow_median ?? 0));
  return Math.min(50, flow_above * (spotRiver.flow_scale ?? 0));
}

// ── FNU → rain equivalent ─────────────────────────────────────────────────────
function riverFnuToRainEquiv(
  spotRiver: RiverConfig | null,
  riverFnus: Record<string, number[]> | null | undefined
): number {
  if (!spotRiver || !riverFnus || !spotRiver.use_fnu || !spotRiver.fnu_gauge) return 0;
  const lag = spotRiver.best_lag ?? 0;
  const fnus = riverFnus[spotRiver.fnu_gauge];
  if (!fnus || fnus[lag] == null || isNaN(fnus[lag])) return 0;
  const fnu = fnus[lag];
  if (fnu < 10) return 0;
  return Math.min(40, fnu * 0.2);
}

// ── NTU → score ───────────────────────────────────────────────────────────────
const NTU_BP: [number, number][] = [
  [0, 100], [2, 90], [5, 75], [10, 58], [20, 40], [40, 22], [80, 10], [150, 0],
];
function ntuToScore(ntu: number): number {
  if (ntu <= NTU_BP[0][0]) return NTU_BP[0][1];
  if (ntu >= NTU_BP[NTU_BP.length - 1][0]) return NTU_BP[NTU_BP.length - 1][1];
  for (let i = 0; i < NTU_BP.length - 1; i++) {
    if (ntu >= NTU_BP[i][0] && ntu <= NTU_BP[i + 1][0]) {
      const t = (ntu - NTU_BP[i][0]) / (NTU_BP[i + 1][0] - NTU_BP[i][0]);
      return Math.round(NTU_BP[i][1] * (1 - t) + NTU_BP[i + 1][1] * t);
    }
  }
  return 0;
}

