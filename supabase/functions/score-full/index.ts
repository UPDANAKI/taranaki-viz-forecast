// ══════════════════════════════════════════════════════════════════════════════
// Vizcast scoring edge function — Lookup Table v2
//
// Replaces the W-block additive formula with per-spot lookup tables derived
// from GradientBoosting models trained on 79,452 satellite observations.
//
// Scoring layers (in order):
//   1. BASE:    lookup tables (swell × rain × wind × SST) → NTU → score
//   2. OVERLAY: satellite turbidity, RTOFS current, tide, recovery, webcam
//   3. APP.JSX: spotSeasonal delta, spotBiasMultiplier (applied client-side)
//
// Lookup table formula:
//   ntu = interp(swell_table, swell_h, swell_p)
//         × interp(rain_table, effective_rain, days_since_rain)
//         + interp(wind_table, wind_onshore, wind_longshore)
//         + interp(sst_table, sst)
//   where effective_rain = rain_24h + river_flow_equiv
//
// All W-block parameters (w_swell, w_wind, w_rain, push_mult etc) are
// no longer used for scoring but remain in App.jsx for documentation.
// ══════════════════════════════════════════════════════════════════════════════

